package com.example.basics;

public class LoopPracticeNames {
	public static void main(String[] args) {

		// for loop
		for (int i = 0; i < 10; i++) {
			System.out.println("hi this is sujatha");
		}

		// while loop
		int i = 0;
		while (i < 10) {
			System.out.println("hi this is sujatha");
			i++;
		}
		int j = 0;
		do {
			System.out.println("hi this is sujatha");
			j++;
		} while (j < 10);

	}

}
